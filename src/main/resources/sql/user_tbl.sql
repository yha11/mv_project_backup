CREATE TABLE USER_TBL(
USER_NUM INT NOT NULL AUTO_INCREMENT PRIMARY KEY, --키값,고유번호
USER_ID VARCHAR(70) NOT NULL UNIQUE, --아이디
USER_NAME VARCHAR(70) NOT NULL, --이름
PASSWORD VARCHAR(70) NOT NULL, --비밀번호
BIRTH DATETIME, --생일
EMAIL VARCHAR(70), --이메일
PHONE VARCHAR(30), --휴대폰번호
REGISTDATE DATETIME DEFAULT NOW(),
ADDR VARCHAR(100), --주소
ADMIN INT DEFAULT 0 --0:사용자, 1:관리자
);

